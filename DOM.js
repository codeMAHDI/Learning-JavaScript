// DOM

// Note: DOM stands for Document Object Model. 
// It is a programming interface for web documents. 
// It represents the structure of a document as a tree of objects, 
// where each object corresponds to a part of the 
// document (such as an element, attribute, or text). 
// The DOM allows developers to manipulate the content, structure, and style 
// of a web page dynamically using JavaScript.
// In short, DOM is a bridge between HTML and JavaScript. 
// It allows JavaScript to read, change, add, or delete HTML elements and styles on a webpage.